//! Data fuzzer: for each value type, drive the edge-case payloads through the
//! harness. Priority 2 of the three modes (CLAUDE-fuzz.md "Data Generator
//! Catalog"). Each catalog entry becomes a minimal sequence: create a hive,
//! create one key, set one value of that type, save. The differential then
//! grades whether both agents store and re-serialize it identically.
//!
//! These sequences are already minimal (one value each), so there is no
//! minimization step; a failure is filed as-is. Catalog entries that find a bug
//! stay as committed regressions.
//!
//! Usage:
//!   data_fuzz [--out DIR] [--emit-catalog] [--gen-only]
//!             [--harness-bin PATH] [--linux-port N] [--windows-host H]

use libreg_fuzz::generators::ops::{ExpectOut, OpSeq};
use libreg_fuzz::generators::values;
use libreg_fuzz::harness_runner::{self, HarnessConfig};
use serde_json::{json, Value};
use std::path::{Path, PathBuf};

struct Args {
    out: PathBuf,
    emit_catalog: bool,
    gen_only: bool,
    harness_bin: PathBuf,
    linux_port: u16,
    windows_host: Option<String>,
    windows_port: u16,
}

impl Default for Args {
    fn default() -> Self {
        Args {
            out: PathBuf::from("tests/fuzz/corpus/pending-data"),
            emit_catalog: false,
            gen_only: false,
            harness_bin: PathBuf::from("tests/harness/target/release/libreg-harness"),
            linux_port: 7878,
            windows_host: None,
            windows_port: 7879,
        }
    }
}

fn fatal(msg: &str) -> ! {
    eprintln!("data_fuzz error: {msg}");
    std::process::exit(2);
}

fn parse_args() -> Args {
    let mut a = Args::default();
    let mut it = std::env::args().skip(1);
    while let Some(arg) = it.next() {
        let mut next = || it.next().unwrap_or_else(|| fatal(&format!("{arg} needs a value")));
        match arg.as_str() {
            "--out" => a.out = PathBuf::from(next()),
            "--emit-catalog" => a.emit_catalog = true,
            "--gen-only" => a.gen_only = true,
            "--harness-bin" => a.harness_bin = PathBuf::from(next()),
            "--linux-port" => a.linux_port = next().parse().unwrap_or_else(|_| fatal("bad --linux-port")),
            "--windows-host" => a.windows_host = Some(next()),
            "--windows-port" => a.windows_port = next().parse().unwrap_or_else(|_| fatal("bad --windows-port")),
            "-h" | "--help" => {
                println!("data_fuzz [--out DIR] [--emit-catalog] [--gen-only] [--harness-bin PATH] [--linux-port N] [--windows-host H]");
                std::process::exit(0);
            }
            other => fatal(&format!("unknown argument: {other}")),
        }
    }
    a
}

/// One catalog entry -> a minimal value_set sequence.
fn seq_for(idx: usize, ty: &str, case: &str, data: &Value) -> OpSeq {
    let safe_ty = ty.to_lowercase();
    let name = format!("data_{idx:03}_{safe_ty}_{case}");
    let path = format!("/tmp/data_{idx:03}_{safe_ty}_{case}.hiv");
    let ops = vec![
        json!({"op": "hive_create", "path": path, "capture": "h"}),
        json!({"op": "key_create", "handle": "$h", "path": "Data"}),
        json!({"op": "value_set", "handle": "$h", "key": "Data",
               "name": case, "type": ty, "data": data}),
        json!({"op": "value_get", "handle": "$h", "key": "Data", "name": case}),
        json!({"op": "hive_save", "handle": "$h"}),
    ];
    OpSeq {
        name,
        tags: vec!["semantic".into(), "structural".into(), "roundtrip".into()],
        operations: ops,
        expect: ExpectOut { semantic_equal: true },
    }
}

/// Emit the catalog as `corpus/interesting/values.yaml` (the committed
/// per-type catalog from CLAUDE-fuzz.md).
fn emit_catalog(catalog: &[(&'static str, String, Value)]) {
    let dir = Path::new("tests/fuzz/corpus/interesting");
    std::fs::create_dir_all(dir).ok();
    let mut doc = String::new();
    doc.push_str("# Per-type value edge-case catalog. Generated by `data_fuzz --emit-catalog`.\n");
    doc.push_str("# Each entry that finds a bug stays here as a regression (CLAUDE-fuzz.md).\n");
    // Group by type for readability.
    let mut last_ty = "";
    for (ty, case, data) in catalog {
        if *ty != last_ty {
            doc.push_str(&format!("\n{ty}:\n"));
            last_ty = ty;
        }
        let data_str = serde_json::to_string(data).unwrap_or_default();
        doc.push_str(&format!("  - {{ name: {case}, data: {data_str} }}\n"));
    }
    let path = dir.join("values.yaml");
    std::fs::write(&path, doc).unwrap_or_else(|e| fatal(&format!("writing catalog: {e}")));
    eprintln!("Wrote catalog to {}", path.display());
}

fn main() {
    let args = parse_args();
    let catalog = values::catalog();

    if args.emit_catalog {
        emit_catalog(&catalog);
    }

    let _ = std::fs::remove_dir_all(&args.out);
    std::fs::create_dir_all(&args.out).ok();
    let mut seqs = Vec::with_capacity(catalog.len());
    for (i, (ty, case, data)) in catalog.iter().enumerate() {
        let seq = seq_for(i, ty, case, data);
        std::fs::write(args.out.join(format!("{}.yaml", seq.name)), seq.to_yaml())
            .unwrap_or_else(|e| fatal(&format!("writing {}: {e}", seq.name)));
        seqs.push(seq);
    }
    eprintln!("Generated {} value-edge sequences into {}", seqs.len(), args.out.display());

    if args.gen_only {
        return;
    }

    // Hermetic run: clear stale hives + transaction logs for these paths so a
    // reload never replays a previous run's log.
    harness_runner::sweep_companions(Path::new("/tmp"), "data_");

    let cfg = HarnessConfig {
        bin: args.harness_bin.clone(),
        linux_host: "127.0.0.1".to_string(),
        linux_port: args.linux_port,
        windows_host: args.windows_host.clone(),
        windows_port: args.windows_port,
        lock_path: PathBuf::from("/tmp/libreg-winvm.lock"),
        extra: Vec::new(),
    };
    let verdicts = harness_runner::run(&cfg, &args.out, &PathBuf::from("tests/fuzz/results-data"))
        .unwrap_or_else(|e| fatal(&format!("running harness: {e}")));
    let failures: Vec<_> = verdicts.iter().filter(|v| v.failed()).collect();
    eprintln!(
        "\n{} value-edge cases graded: {} passed, {} failed",
        verdicts.len(),
        verdicts.len() - failures.len(),
        failures.len()
    );
    for v in &failures {
        eprintln!("  FAIL {} [{}]: {}", v.name, v.kind.unwrap().as_str(), v.detail.replace('\n', " | "));
    }
    if !failures.is_empty() {
        std::process::exit(1);
    }
}
